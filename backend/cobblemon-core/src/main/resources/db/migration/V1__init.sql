CREATE TABLE spawn_rule (
                            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                            spawn_source_file_id BIGINT NOT NULL,

                            external_id VARCHAR(40) NOT NULL,

                            pokemon_id BIGINT NOT NULL,
                            pokemon_form_id BIGINT NULL,

                            target_expression VARCHAR(120) NOT NULL,

                            spawn_type VARCHAR(20) NOT NULL,
                            spawnable_position_type VARCHAR(20) NOT NULL,
                            bucket VARCHAR(20) NOT NULL,

                            level_min SMALLINT NOT NULL,
                            level_max SMALLINT NOT NULL,

                            weight NUMERIC(8,3) NOT NULL,

                            max_herd_size SMALLINT NULL,
                            min_distance_between_spawns NUMERIC(6,2) NULL,

                            weight_multiplier_json JSONB NULL,
                            weight_multipliers_json JSONB NULL,
                            herdable_pokemon_json JSONB NULL,

                            raw_json JSONB NOT NULL,

                            CONSTRAINT fk_spawn_rule_source_file
                                FOREIGN KEY (spawn_source_file_id) REFERENCES spawn_source_file(id),

                            CONSTRAINT fk_spawn_rule_pokemon
                                FOREIGN KEY (pokemon_id) REFERENCES pokemon(id),

                            CONSTRAINT fk_spawn_rule_pokemon_form
                                FOREIGN KEY (pokemon_form_id) REFERENCES pokemon_form(id),

                            CONSTRAINT uq_spawn_rule_external_id_per_file UNIQUE (spawn_source_file_id, external_id),

                            CONSTRAINT ck_spawn_rule_spawn_type
                                CHECK (spawn_type IN ('pokemon', 'pokemon-herd')),

                            CONSTRAINT ck_spawn_rule_position_type
                                CHECK (spawnable_position_type IN ('grounded', 'surface', 'submerged', 'seafloor', 'fishing')),

                            CONSTRAINT ck_spawn_rule_bucket
                                CHECK (bucket IN ('common', 'uncommon', 'rare', 'ultra-rare')),

                            CONSTRAINT ck_spawn_rule_level
                                CHECK (level_min >= 1 AND level_max >= level_min),

                            CONSTRAINT ck_spawn_rule_weight
                                CHECK (weight > 0)
);

CREATE TABLE spawn_rule_preset (
                                   spawn_rule_id BIGINT NOT NULL,
                                   spawn_preset_id BIGINT NOT NULL,

                                   PRIMARY KEY (spawn_rule_id, spawn_preset_id),

                                   CONSTRAINT fk_spawn_rule_preset_rule
                                       FOREIGN KEY (spawn_rule_id) REFERENCES spawn_rule(id),

                                   CONSTRAINT fk_spawn_rule_preset_preset
                                       FOREIGN KEY (spawn_preset_id) REFERENCES spawn_preset(id)
);

CREATE TABLE spawn_condition (
                                 id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                 spawn_rule_id BIGINT NOT NULL,

                                 can_see_sky BOOLEAN NULL,
                                 is_raining BOOLEAN NULL,
                                 is_thundering BOOLEAN NULL,
                                 is_slime_chunk BOOLEAN NULL,

                                 min_x INTEGER NULL,
                                 max_x INTEGER NULL,
                                 min_y SMALLINT NULL,
                                 max_y SMALLINT NULL,

                                 min_light SMALLINT NULL,
                                 max_light SMALLINT NULL,
                                 min_sky_light SMALLINT NULL,
                                 max_sky_light SMALLINT NULL,

                                 min_lure_level SMALLINT NULL,
                                 max_lure_level SMALLINT NULL,

                                 moon_phase VARCHAR(16) NULL,
                                 time_range VARCHAR(16) NULL,
                                 rod_type VARCHAR(30) NULL,
                                 bait_item_expression VARCHAR(64) NULL,

                                 condition_json JSONB NULL,
                                 anticondition_json JSONB NULL,
                                 effective_condition_json JSONB NULL,
                                 effective_anticondition_json JSONB NULL,


                                 CONSTRAINT fk_spawn_condition_rule
                                     FOREIGN KEY (spawn_rule_id) REFERENCES spawn_rule(id),

                                 CONSTRAINT uq_spawn_condition_rule UNIQUE (spawn_rule_id),

                                 CONSTRAINT ck_spawn_condition_light
                                     CHECK (
                                         (min_light IS NULL OR min_light BETWEEN 0 AND 15)
                                             AND (max_light IS NULL OR max_light BETWEEN 0 AND 15)
                                             AND (min_sky_light IS NULL OR min_sky_light BETWEEN 0 AND 15)
                                             AND (max_sky_light IS NULL OR max_sky_light BETWEEN 0 AND 15)
                                         ),

                                 CONSTRAINT ck_spawn_condition_y
                                     CHECK (
                                         (min_y IS NULL OR min_y >= -512)
                                             AND (max_y IS NULL OR max_y <= 512)
                                         ),

                                CONSTRAINT ck_spawn_condition_x_order
                                     CHECK (min_x IS NULL OR max_x IS NULL OR min_x <= max_x),

                                 CONSTRAINT ck_spawn_condition_y_order
                                     CHECK (min_y IS NULL OR max_y IS NULL OR min_y <= max_y),

                                 CONSTRAINT ck_spawn_condition_light_order
                                     CHECK (min_light IS NULL OR max_light IS NULL OR min_light <= max_light),

                                 CONSTRAINT ck_spawn_condition_sky_light_order
                                     CHECK (min_sky_light IS NULL OR max_sky_light IS NULL OR min_sky_light <= max_sky_light),

                                 CONSTRAINT ck_spawn_condition_lure_order
                                     CHECK (min_lure_level IS NULL OR max_lure_level IS NULL OR min_lure_level <= max_lure_level)

);

CREATE TABLE spawn_condition_token (
                                       id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                       spawn_condition_id BIGINT NOT NULL,

                                       side VARCHAR(16) NOT NULL,
                                       token_type VARCHAR(24) NOT NULL,
                                       token_value VARCHAR(80) NOT NULL,
                                       is_tag BOOLEAN NOT NULL DEFAULT FALSE,

                                       CONSTRAINT fk_spawn_condition_token_condition
                                           FOREIGN KEY (spawn_condition_id) REFERENCES spawn_condition(id),

                                       CONSTRAINT ck_spawn_condition_token_side
                                           CHECK (side IN ('condition', 'anticondition')),

                                       CONSTRAINT ck_spawn_condition_token_type
                                           CHECK (token_type IN ('biome', 'structure', 'dimension', 'nearby_block', 'base_block', 'label'))
);

CREATE TABLE seasoning (
                           id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                           item_id BIGINT NOT NULL,
                           colour VARCHAR(20) NOT NULL,
