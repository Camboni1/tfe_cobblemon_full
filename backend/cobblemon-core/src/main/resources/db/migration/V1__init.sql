CREATE TABLE dataset_version (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(16) NOT NULL,
    label VARCHAR(32) NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT FALSE,
    imported_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_dataset_version_code UNIQUE (code)
    );

CREATE TABLE item (
    id BIGINT Generated BY DEFAULT AS IDENTITY PRIMARY KEY,
    dataset_version_id BIGINT NOT NULL,
    namespaced_id VARCHAR(64) NOT NULL,
    namespace VARCHAR(24) NOT NULL,
    path varchar(64) NOT NULL,
    display_name VARCHAR(80) NULL,
    raw_json JSONB NULL,

    CONSTRAINT fk_item_dataset_version FOREIGN KEY (dataset_version_id) REFERENCES dataset_version(id),

    CONSTRAINT uq_item_version_namespaced_id UNIQUE (dataset_version_id, namespaced_id)
);

CREATE TABLE pokemon (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dataset_version_id BIGINT NOT NULL,
    slug varchar(50) NOT NULL,
    display_name VARCHAR(50) NOT NULL,
    national_dex_number smallint NOT NULL,
    generation_code VARCHAR(12) NOT NULL,
    implemented BOOLEAN NOT NULL DEFAULT TRUE,
    source_file VARCHAR(64) NOT NULL,
    raw_json JSONB NOT NULL,

    CONSTRAINT fk_pokemon_dataset_version FOREIGN KEY (dataset_version_id) REFERENCES dataset_version(id),
    CONSTRAINT uq_pokemon_version_slug UNIQUE (dataset_version_id, slug),
    CONSTRAINT uq_pokemon_version_dex UNIQUE (dataset_version_id, national_dex_number)
);

CREATE TABLE pokemon_form (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pokemon_id BIGINT NOT NULL,
    code VARCHAR(50) NOT NULL,
    display_name VARCHAR(80) NOT NULL,
    is_default BOOLEAN NOT NULL DEFAULT FALSE,
    battle_only BOOLEAN NOT NULL DEFAULT FALSE,

    primary_type VARCHAR(16) NOT NULL,
    secondary_type VARCHAR(16) NULL,

    male_ratio NUMERIC(4,3) NULL,
    height_dm INTEGER NULL,
    weight_hg INTEGER NULL,

    catch_rate SMALLINT NULL,
    base_experience_yield SMALLINT NULL,
    experience_group VARCHAR(20) NULL,
    egg_cycles SMALLINT NULL,
    base_friendship SMALLINT NULL,
    base_scale NUMERIC(5,3) NULL,

    base_hp SMALLINT NOT NULL,
    base_attack SMALLINT NOT NULL,
    base_defense SMALLINT NOT NULL,
    base_special_attack SMALLINT NOT NULL,
    base_special_defense SMALLINT NOT NULL,
    base_speed SMALLINT NOT NULL,

    ev_hp SMALLINT NOT NULL DEFAULT 0,
    ev_attack SMALLINT NOT NULL DEFAULT 0,
    ev_defense SMALLINT NOT NULL DEFAULT 0,
    ev_special_attack SMALLINT NOT NULL DEFAULT 0,
    ev_special_defense SMALLINT NOT NULL DEFAULT 0,
    ev_speed SMALLINT NOT NULL DEFAULT 0,

    aspects_json JSONB NULL,
    labels_json JSONB NULL,
    raw_json JSONB NOT NULL,

    CONSTRAINT fk_pokemon_form_pokemon FOREIGN KEY (pokemon_id) REFERENCES pokemon(id),
    CONSTRAINT uq_pokemon_form_code UNIQUE (pokemon_id, code),
    CONSTRAINT ck_pokemon_form_male_ratio CHECK ( male_ratio IS NULL OR (male_ratio >= -1.00 AND male_ratio <= 1.00) ),
    CONSTRAINT ck_pokemon_form_base_stats CHECK (
        base_hp BETWEEN 1 AND 255
        AND base_attack BETWEEN 1 AND 255
        AND base_defense BETWEEN 1 AND 255
        AND base_special_attack BETWEEN 1 AND 255
        AND base_special_defense BETWEEN 1 AND 255
        AND base_speed BETWEEN 1 AND 255
        ),

    CONSTRAINT ck_pokemon_form_ev_stats
                          CHECK (
                              ev_hp BETWEEN 0 AND 3
                              AND ev_attack BETWEEN 0 AND 3
                              AND ev_defense BETWEEN 0 AND 3
                              AND ev_special_attack BETWEEN 0 AND 3
                              AND ev_special_defense BETWEEN 0 AND 3
                              AND ev_speed BETWEEN 0 AND 3
                              ),

    CONSTRAINT ck_pokemon_form_catch_rate
                          CHECK ( catch_rate IS NULL OR catch_rate BETWEEN 0 AND 255),

    CONSTRAINT ck_pokemon_form_base_friendship
                          CHECK ( base_friendship IS NULL OR base_friendship BETWEEN 0 AND 255)
);

CREATE TABLE pokemon_drop (
                              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                              pokemon_form_id BIGINT NOT NULL,
                              item_id BIGINT NOT NULL,

                              drop_pool_amount_min SMALLINT NULL,
                              drop_pool_amount_max SMALLINT NULL,

                              quantity_min SMALLINT NULL,
                              quantity_max SMALLINT NULL,

                              percentage NUMERIC(5,2) NULL,
                              raw_json JSONB NOT NULL,

                              CONSTRAINT fk_pokemon_drop_form
                                  FOREIGN KEY (pokemon_form_id) REFERENCES pokemon_form(id),

                              CONSTRAINT fk_pokemon_drop_item
                                  FOREIGN KEY (item_id) REFERENCES item(id),

                              CONSTRAINT ck_pokemon_drop_percentage
                                  CHECK (percentage IS NULL OR (percentage >= 0 AND percentage <= 100))
);

